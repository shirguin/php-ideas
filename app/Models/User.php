        //return $this->belongsToMany(User::class, 'follower_user', '', '');
    }

    public function followers()
    {

    }

    public function getImageUrl()
    {
        if ($this->image) {
            return url('storage/' . $this->image);
        }
        return "https://api.dicebear.com/6.x/fun-emoji/svg?seed={{$this->name}}";
    }
}
